const FACTIONS = {
  goguryeo: { faction: '고구려(高句麗)', color: '#ea3939' },
  baekje:   { faction: '백제(百濟)',   color: '#ffff00' },
  silla:    { faction: '신라(新羅)',   color: '#758af0' },
  goryeo:   { faction: '고려(高麗)',   color: '#eeff00' },
};

/* ─────────────────────────────────────────────
   메인
───────────────────────────────────────────── */
async function main() {
  const client = new MongoClient(MONGO_URI);
  await client.connect();
  const db = client.db(DB_NAME);
  const heroes    = db.collection('heroes');
  const heroPos   = db.collection('hero_positions');

  const now = new Date();
  let created = 0, skipped = 0, posInserted = 0;

  async function processKings(kings, factionKey, capFn) {
    const { faction, color } = FACTIONS[factionKey];

    for (const k of kings) {
      const { nameKo, nameZh, title } = parseName(k.raw);

      // 중복 체크 (name_ko + birth_year)
      const existing = await heroes.findOne({ name_ko: nameKo, birth_year: k.start });
      if (existing) {
        console.log(`  ⏭ 이미 존재: ${nameKo} (${k.start}~${k.end})`);
        skipped++;
        continue;
      }

      // 영웅 삽입
      const heroDoc = {
        name_ko:      nameKo,
        name_zh:      nameZh,
        birth_year:   k.start,
        death_year:   k.end,
        title:        title,
        description:  k.desc || '',
        faction,
        faction_color: color,
        avatar_url:   '',
        illustration_url: '',
        vote_count:   0,
        createdAt:    now,
      };
      const result = await heroes.insertOne(heroDoc);
      const heroId = result.insertedId;
      created++;

      // 수도 위치 삽입
      const cap = capFn(k.start);
      const posDoc = {
        hero_id:       heroId,
        start_year:    k.start,
        end_year:      k.end,
        year:          k.start,
        type:          'STAY',
        event_title:   `${title} 즉위`,
        location_name: cap.name,
        geometry: {
          type: 'Point',
          coordinates: [cap.lng, cap.lat],
        },
        source_text:   '',
        createdAt:     now,
      };
      await heroPos.insertOne(posDoc);
      posInserted++;

      console.log(`  ✅ ${nameKo} (${k.start}~${k.end}) → ${cap.name}`);
    }
  }

  console.log('\n▶ 고구려 왕 삽입...');
  await processKings(GOGURYEO_KINGS, 'goguryeo', ggCapital);

  console.log('\n▶ 백제 왕 삽입...');
  await processKings(BAEKJE_KINGS, 'baekje', bjCapital);

  console.log('\n▶ 신라 왕 삽입...');
  await processKings(SILLA_KINGS, 'silla', () => CAP.sr_gyeongju);

  console.log('\n▶ 고려 왕 삽입...');
  await processKings(GORYEO_KINGS, 'goryeo', () => CAP.gr_gaebyeong);

  console.log(`\n🎉 완료 — 영웅 생성: ${created}명, 건너뜀: ${skipped}명, 위치 삽입: ${posInserted}건`);
  await client.close();
}

main().catch(err => { console.error('❌ 오류:', err); process.exit(1); });
