/* ─────────────────────────────────────────────
   이름 파서
   입력 예: "19대 광개토경평안호태왕 (廣開土大王) / 고담덕"
   출력: { nameKo, nameZh, title }
───────────────────────────────────────────── */
function parseName(raw) {
  // 1) 대수 접두어 제거
  let s = raw.replace(/^\d+대[\t\s]*/u, '').trim();

  // 2) / 이후 → 개인명
  let personalKo = '';
  if (s.includes('/')) {
    personalKo = s.split('/').pop().trim();
    s = s.split('/')[0].trim();
  } else {
    const tokens = s.split(/\s+/).filter(Boolean);
    if (tokens.length >= 2) {
      const lastToken = tokens[tokens.length - 1];
      const head = tokens.slice(0, -1).join(' ');
      if (/[가-힣]{2,4}/.test(lastToken) && /(거서간|차차웅|이사금|마립간|태왕|대왕|왕|진왕|여왕|대제|태제|황제)/.test(head)) {
        personalKo = lastToken;
        s = head;
      }
    }
  }

  // 3) 한자 괄호 추출 (첫 번째)
  let nameZh = '';
  const zhMatch = s.match(/[（(]([^）)]+)[）)]/);
  if (zhMatch) {
    const inner = zhMatch[1].trim();
    if (/[\u4e00-\u9fff]/.test(inner)) nameZh = inner;
  }

  // 4) 괄호 전체 제거 → 칭호
  const title = s.replace(/[（(][^）)]*[）)]/g, '').trim();

  // 5) 표시용 한국어 이름
  const nameKo = personalKo || title;

  return { nameKo, nameZh, title };
}

